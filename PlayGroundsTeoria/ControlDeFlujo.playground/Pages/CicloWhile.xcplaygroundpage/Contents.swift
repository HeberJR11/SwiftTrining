// Bucle While
// Esta sentencia ejecuta un conjunto de instrucciones hasta que se convierta una condicion que es verdadera en false

var age = 10

while age < 18 {
    print("Eres menor de edad. Te faltan \(18 - age) años")
    age += 1
}

print("Ya eres mayor de edad")

