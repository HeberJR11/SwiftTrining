import Foundation

// Arrays: colecciones ordenadas y si se pueden repetir

// Generacion de un array
var arregloNumeros = [1,2,3]
arregloNumeros.append(5) //Inserta un elemento al arreglo
arregloNumeros.insert(4, at: 3) //Inserta el numero 4 en la piscion 3 del arreglo
arregloNumeros.removeLast() //Elimina el ultimo elemento del arreglo
arregloNumeros.remove(at: 3) //Elimina el elemento que este en la posicion 3 del arreglo
arregloNumeros.removeAll() //Elimina todos lo elementos del arreglo
